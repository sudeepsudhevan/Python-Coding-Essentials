# Find the Biggest of 2 numbers

num1 = int(input('Enter the first number: '))
num2 = int(input('Enter the second number: '))

if num1 > num2:
    print(f'Largest number is {num1}')
elif num1 < num2:
    print(f'Largest number is {num2}')
else:
    print('Both are Same')












