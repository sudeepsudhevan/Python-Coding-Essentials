num1 = int(input('Enter the First Number: '))
num2 = int(input('Enter the Second Number: '))

print(f'Initially num1 is {num1}')
print(f'Initially num2 is {num2}')

swap = num1
num1 = num2
num2 = swap

print(f'\nNow num1 is {num1}')
print(f'Now num2 is {num2}')










